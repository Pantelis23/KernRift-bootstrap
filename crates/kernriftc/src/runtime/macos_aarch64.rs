    // Unlike Linux clone, macOS fork returns in both processes.
    // child has x0=0, parent has x0=pid.
    // .child at 0x184 (was 0x198 in Linux - need to recompute)
    // Wait, I declared cbz target as 0x198 above. Let me recount parent instructions.
    // From 0x13C to 0x180: that's (0x184 - 0x13C)/4 = 0x48/4 = 18 instructions.
