    // after the parent ret. Parent ret is at 0x180. Next instruction = 0x184.
    // But I declared cbz offset as 0x198 - 0x138 = 0x60 = 96.
    // That means .child = 0x198. But the parent block only goes to 0x183.
    // I need either NOPs from 0x184 to 0x197, or change the cbz target.
    // Let me fix: .child should be at 0x184, not 0x198.
    // cbz at 0x138: offset = 0x184 - 0x138 = 0x4C = 76, imm19 = 19
    //   = 0xB4000260
