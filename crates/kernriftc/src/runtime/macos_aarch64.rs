    // === __kr_str_copy at 0x1D8 ===
    // (identical to Linux AArch64)
    c[0x1D8] = 0xE2; c[0x1D9] = 0x03; c[0x1DA] = 0x00; c[0x1DB] = 0xAA; // mov x2, x0
