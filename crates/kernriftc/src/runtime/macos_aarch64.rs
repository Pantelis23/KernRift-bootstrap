    c[0x1B4] = 0x01; c[0x1B5] = 0x10; c[0x1B6] = 0x00; c[0x1B7] = 0xD4;
    // 0x1B8: mov x0, #127 = 0xD2800FE0
    c[0x1B8] = 0xE0; c[0x1B9] = 0x0F; c[0x1BA] = 0x80; c[0x1BB] = 0xD2;
