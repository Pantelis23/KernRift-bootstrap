#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn blob_size_is_correct() {
        assert_eq!(BLOB.code.len(), 632);
    }

    #[test]
    fn all_symbols_within_bounds() {
        assert_eq!(BLOB.symbols.len(), 11);
        for &(name, offset) in BLOB.symbols {
            assert!(
                (offset as usize) < BLOB.code.len(),
                "symbol {name} offset 0x{offset:X} out of bounds",
            );
        }
    }

    #[test]
    fn main_call_fixup_within_bounds() {
        assert!((BLOB.main_call_fixup as usize + 4) <= BLOB.code.len());
    }

    #[test]
    fn bl_main_opcode_correct() {
        let off = BLOB.main_call_fixup as usize;
        let instr = u32::from_le_bytes([
            BLOB.code[off], BLOB.code[off+1], BLOB.code[off+2], BLOB.code[off+3],
        ]);
        assert_eq!(instr, 0x94000000, "expected BL placeholder at 0x{off:X}");
    }

    #[test]
    fn data_area_is_zeroed() {
        let data_start = BLOB.code.len() - 24;
        for i in data_start..BLOB.code.len() {
            assert_eq!(BLOB.code[i], 0x00);
        }
    }

    #[test]
    fn dealloc_is_just_ret() {
        let off = BLOB.symbol_offset("__kr_dealloc").unwrap() as usize;
        let instr = u32::from_le_bytes([
            BLOB.code[off], BLOB.code[off+1], BLOB.code[off+2], BLOB.code[off+3],
        ]);
        assert_eq!(instr, 0xD65F03C0);
    }

    #[test]
    fn inline_strings_present() {
        assert_eq!(&BLOB.code[0x250..0x258], b"/bin/sh\0");
        assert_eq!(&BLOB.code[0x258..0x25B], b"-c\0");
    }

    #[test]
    fn symbol_names_complete() {
        let expected = [
            "_start", "__kr_exit", "__kr_write", "__kr_mmap_alloc",
            "__kr_alloc", "__kr_dealloc", "__kr_getenv", "__kr_exec",
            "__kr_str_copy", "__kr_str_cat", "__kr_str_len",
        ];
        for name in &expected {
            assert!(BLOB.symbol_offset(name).is_some(), "missing symbol: {name}");
        }
    }

    #[test]
    fn svc_0x80_encoding() {
        // SVC #0x80 should be 0xD4001001 (not 0xD4000001 like Linux)
        let write_off = BLOB.symbol_offset("__kr_write").unwrap() as usize;
        let svc_off = write_off + 4; // after mov x16, #4
        let instr = u32::from_le_bytes([
            BLOB.code[svc_off], BLOB.code[svc_off+1],
            BLOB.code[svc_off+2], BLOB.code[svc_off+3],
        ]);
        assert_eq!(instr, 0xD4001001, "expected SVC #0x80");
    }

    #[test]
    fn all_instructions_4byte_aligned() {
        for &(name, offset) in BLOB.symbols {
            assert_eq!(offset % 4, 0, "symbol {name} at 0x{offset:X} not aligned");
        }
    }
}
