    //   immlo = 208 & 3 = 0, immhi = 208 >> 2 = 52
    //   = 0x10000000 | (52 << 5) | 6 = 0x10000686
    c[0x188] = 0x86; c[0x189] = 0x06; c[0x18A] = 0x00; c[0x18B] = 0x10;
    // 0x18C: sub sp, sp, #32 = 0xD10083FF
    c[0x18C] = 0xFF; c[0x18D] = 0x83; c[0x18E] = 0x00; c[0x18F] = 0xD1;
    // 0x190: str x5, [sp] = 0xF90003E5
    c[0x190] = 0xE5; c[0x191] = 0x03; c[0x192] = 0x00; c[0x193] = 0xF9;
    // 0x194: str x6, [sp, #8] = 0xF90007E6
    c[0x194] = 0xE6; c[0x195] = 0x07; c[0x196] = 0x00; c[0x197] = 0xF9;
    // 0x198: str x19, [sp, #16] = 0xF9000BF3
    c[0x198] = 0xF3; c[0x199] = 0x0B; c[0x19A] = 0x00; c[0x19B] = 0xF9;
    // 0x19C: str xzr, [sp, #24] = 0xF9000FFF
    c[0x19C] = 0xFF; c[0x19D] = 0x0F; c[0x19E] = 0x00; c[0x19F] = 0xF9;
    // 0x1A0: mov x0, x5 (filename) = 0xAA0503E0
    c[0x1A0] = 0xE0; c[0x1A1] = 0x03; c[0x1A2] = 0x05; c[0x1A3] = 0xAA;
